primeiro_numero = float(input("Digite um número: "))
segundo_numero = float(input("Digite outro número: "))

#adição
soma = primeiro_numero + segundo_numero
print(f'A soma dos números é: {soma}')

#subtração
sub = primeiro_numero - segundo_numero
print(f'A subtração dos números é: {sub}')


#mutiplicação
mult = primeiro_numero * segundo_numero
print(f'A multiplicação dos números é: {mult}')

#divisão
div = primeiro_numero / segundo_numero 
print(f'A divisão dos números é: {div}')

#resto da divisão
resto = primeiro_numero % segundo_numero
print(f'O resto da divisão dos números é: {resto}')