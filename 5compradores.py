primeiro_numero = float(input("Digite o valor do primeiro produto: "))
segundo_numero = float(input("Digite o valor do segundo produto: "))

#maior que
print(primeiro_numero > segundo_numero)

#menor que
print(primeiro_numero < segundo_numero)

#mairo ou igual
print(primeiro_numero >= segundo_numero)
#menor ou igual
print(primeiro_numero <= segundo_numero)

#igual
print(primeiro_numero == segundo_numero)

#diferente de
print(primeiro_numero != segundo_numero)